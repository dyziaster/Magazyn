package Entitys;

public class TowarRodzaj {

	    private String produkcja;
	    private String spedycja;
	    private String skladowanie;
	    private String infrastruktura;
	    private String inwestycje;
	    public String getProdukcja() {
	            return produkcja;
	    }
	    public void setProdukcja(String produkcja) {
	            this.produkcja = produkcja;
	    }
	    public String getSpedycja() {
	            return spedycja;
	    }
	    public void setSpedycja(String spedycja) {
	            this.spedycja = spedycja;
	    }
	    public String getSkladowanie() {
	            return skladowanie;
	    }
	    public void setSkladowanie(String skladowanie) {
	            this.skladowanie = skladowanie;
	    }
	    public String getInfrastruktura() {
	            return infrastruktura;
	    }
	    public void setInfrastruktura(String infrastruktura) {
	            this.infrastruktura = infrastruktura;
	    }
	    public String getInwestycje() {
	            return inwestycje;
	    }
	    public void setInwestycje(String inwestycje) {
	            this.inwestycje = inwestycje;
	    }



}
