package Entitys;

public class TowarTyp {
	

	    private String surowiec;
	    private String wyrob;
	    private String material;
	    private String wyposazenie;
	    private String remonty;
	    public String getSurowiec() {
	            return surowiec;
	    }
	    public void setSurowiec(String surowiec) {
	            this.surowiec = surowiec;
	    }
	    public String getWyrob() {
	            return wyrob;
	    }
	    public void setWyrob(String wyrob) {
	            this.wyrob = wyrob;
	    }
	    public String getMaterial() {
	            return material;
	    }
	    public void setMaterial(String material) {
	            this.material = material;
	    }
	    public String getWyposazenie() {
	            return wyposazenie;
	    }
	    public void setWyposazenie(String wyposazenie) {
	            this.wyposazenie = wyposazenie;
	    }
	    public String getRemonty() {
	            return remonty;
	    }
	    public void setRemonty(String remonty) {
	            this.remonty = remonty;
	    }
	

}
